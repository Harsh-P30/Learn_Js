function name(a) {
    switch (a) {
        case 3:
            console.log("its 3");
            break;
        case 2:
            console.log("its 2");
            break;
        case 4:
            console.log("its 4");
            break;
        case 5:
            console.log("its 5");
            break;
    
        default:
            console.log("more than 5")
            break;
    }
}

name(8);
name(2);
name(4);

